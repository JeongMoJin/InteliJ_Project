package chapter01;

public class Exam006 {
	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;
		boolean c = !b; // b의 값을 반대로 입력
		
		System.out.println(a); // true
		System.out.println(!a); // false
		
		System.out.println(b); // false 
		System.out.println(c); // true
		
	}
}
